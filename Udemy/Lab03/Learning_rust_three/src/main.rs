fn main() {
    let y: i32 = 15;
    println!("This is a int 32 {}", y);

    let x: f32 = 15.0;
    println!("This is a float 32 {}", x);

    let mut z: f32 = 17.0;
    println!("This is a float 32 {}", z);

    z = 18.0;
    println!("This is a float 32 {} which I've updated", z);

    let y: i32 = 5 * 5;
}
